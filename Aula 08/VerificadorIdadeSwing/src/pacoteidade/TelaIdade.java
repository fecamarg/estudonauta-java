        // TODO add your handling code here:
        int ano = Integer.parseInt(txtAno.getText()); //Converter a string para inteiro
        int idade = 2023 - ano;
        lblIdade.setText(Integer.toString(idade)); //Converter inteiro para string
        String sit = ((idade>=16 && idade<18) || (idade>70))?"Opcional":"Obrigatório";
        // Se a idade for maior ou igual 16 e menor que 18, ou maior que 70
        lblSituacao.setText(sit);
